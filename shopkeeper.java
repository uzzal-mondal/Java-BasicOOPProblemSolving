package JavaOOP;

public class shopkeeper {


     // method overloading

    void add(){
        System.out.println("This is default constructer");
    }

    void add(int a, int b){

        System.out.println(a+b);
    }

    void add(double a, double b, double c){

        System.out.println(a+b+c);
    }




}
